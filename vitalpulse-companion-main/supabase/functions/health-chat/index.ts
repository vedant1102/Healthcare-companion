import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are HealthMate, a compassionate AI health companion. Your role is to:

1. Analyze symptoms and suggest possible conditions
2. Recommend over-the-counter medicines when appropriate
3. Suggest home remedies and wellness practices
4. Provide health information and guidance
5. Always remind users to consult healthcare professionals for serious concerns

IMPORTANT GUIDELINES:
- Be empathetic and supportive
- Never diagnose definitively - use phrases like "might indicate", "could be", "possibly"
- Always include a disclaimer about consulting real doctors
- Provide varied, dynamic responses - don't repeat the same suggestions
- Consider symptom combinations when making suggestions
- Include both medical and natural/home remedy options
- BE EXTREMELY CONCISE - use bullet points for everything
- Use **bold** to highlight important medical terms, conditions, and medicine names
- Keep responses short and scannable

RESPONSE FORMAT (ALWAYS USE THIS):
**Possible Conditions:**
• **[Condition Name]**: Brief description (1 line max)

**Recommended Medicines:**
• **[Medicine Name]**: Dosage (be specific and concise)

**Home Remedies:**
• Brief remedy (1 line max)

**⚠️ Disclaimer:** This is not professional medical advice. Consult a doctor for serious concerns.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required. Please add credits to your workspace." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Health chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
